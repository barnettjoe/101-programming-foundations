def reverse(arr)
  arr.each_with_object([]) { |el, rev_arr| rev_arr.unshift(el) }
end

#or...
def reverse(arr)
  arr.inject([]) { |new_arr, el| new_arr.unshift el  }
end



puts reverse([1,2,3,4]) == [4,3,2,1]          # => true
puts reverse(%w(a b c d e)) == %w(e d c b a)  # => true
puts reverse(['abc']) == ['abc']              # => true
puts reverse([]) == []                        # => true

p list = [1, 2, 3]                      # => [1, 2, 3]
p new_list = reverse(list)              # => [3, 2, 1]
puts list.object_id != new_list.object_id  # => true
puts list == [1, 2, 3]                     # => true
puts new_list == [3, 2, 1]                 # => true